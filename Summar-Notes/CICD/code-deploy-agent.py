

# Code deploy Agent
    # To use CodeDeploy on EC2 instances or on-premises servers, the CodeDeploy agent must be installed first. 
    # We recommend installing and updating the CodeDeploy agent with AWS Systems Manager. You can set up installation 
    # and scheduled updates of the CodeDeploy agent with Systems Manager in the console when you create your deployment 
    # groups.You can also install the CodeDeploy agent directly from an S3 bucket with the command line.
# AWS Systems Manager 
    # It is a collection of capabilities to help you manage your applications and infrastructure running in the 
    # AWS Cloud. Systems Manager simplifies application and resource management, shortens the time to detect and 
    # resolve operational problems, and helps you manage your AWS resources securely at scale.