

# CloudFormation Designers
    # AWS CloudFormation Designer (Designer) is a graphic tool for creating, viewing, and modifying AWS CloudFormation 
    # templates. With Designer, you can diagram your template resources using a drag-and-drop interface, and then edit 
    # their details using the integrated JSON and YAML editor. Whether you are a new or an experienced AWS CloudFormation 
    # user, AWS CloudFormation Designer can help you quickly see the interrelationship between a template’s resources 
    # and easily modify templates.
    # Basically, it allows you to create CloudFormation templates in a visual fashion or to visualize your existing 
    # CloudFormation stacks in a visual fashion. So it’s quite handy if you want to quickly draft the CloudFormation 
    # template and to make sure that your template is well-written although this all can also be addressed by your code 
    # editor.