
# Docker
    # Docker is an open source platform for developing, shipping, and running applications in a loosely isolated 
    # environment called a container. Amazon Web Services (AWS) is a natural complement to containers and offers 
    # a wide range of scalable infrastructure services upon which containers can be deployed. You will find 
    # various options such as AWS Elastic Beanstalk, Amazon Elastic Container Service (Amazon ECS), Amazon Elastic 
    # Kubernetes Service (Amazon EKS), AWS Fargate (Fargate), and AWS App Runner. This paper cover details of each 
    # option and key components of the container orchestration.