
# Amazon Relational Database Service (Amazon RDS) is a web service that makes it easier to set up, operate, and 
# scale a relational database in the AWS Cloud. It provides cost-efficient, resizable capacity for an industry
# -standard relational database and manages common database administration tasks.
# DB engines
    # A DB engine is the specific relational database software that runs on your DB instance. Amazon RDS currently 
    # supports the following engines:
        # MariaDB
        # Microsoft SQL Server
        # MySQL
        # Oracle
        # PostgreSQL
        # Aurora
    # Each DB engine has its own supported features, and each version of a DB engine can include specific features. 
# DB instance storage
    # Amazon EBS provides durable, block-level storage volumes that you can attach to a running instance. DB instance 
    # storage comes in the following types:
        # General Purpose (SSD)
        # Provisioned IOPS (PIOPS)
        # Magnetic