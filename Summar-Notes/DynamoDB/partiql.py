

# PartiQL
    # Amazon DynamoDB supports PartiQL, a SQL-compatible query language, to select, insert, update, and delete data in 
    # Amazon DynamoDB. Using PartiQL, you can easily interact with DynamoDB tables and run ad hoc queries using the AWS 
    # Management Console, NoSQL Workbench, AWS Command Line Interface, and DynamoDB APIs for PartiQL.
    # PartiQL operations provide the same availability, latency, and performance as the other DynamoDB data plane 
    # operations.